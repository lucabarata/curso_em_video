import turtle
from time import sleep
a = turtle.Turtle()
a.speed(0)
for i in range(230):
    a.forward(2+i/4)
    a.left(30-i/12)
a.penup()
a.home()
a.pendown()
a.setheading(45)
a.forward(200)
a.home()
a.setheading(45+90)
a.forward(200)
a.home()
a.setheading(45+(90*2))
a.forward(200)
a.home()
a.setheading(45+(90*3))
a.forward(200)
a.home()
a.pencolor("blue")
a.dot(40)
sleep(5)
turtle.done()